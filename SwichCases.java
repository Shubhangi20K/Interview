package com.multi;

public class SwichCases {
	public static void main(String[] args) {

		int x=1;

		switch (x) {
		case 1:
			System.out.println("Case 1");

	
		case 2 :
			System.out.println("case 2");
			break;

		default:
			System.out.println("default");
			break;
		}

	}

}
